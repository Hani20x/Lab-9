import tkinter as tk
from tkinter import scrolledtext # Можно использовать для более длинных лент

# --- Функция для очистки текстового поля ---
def clear_messages():
    """Удаляет все содержимое из виджета Text (ленты сообщений)."""
    # Метод delete удаляет текст в указанном диапазоне.
    # '1.0' означает начало текста (строка 1, символ 0).
    # tk.END означает конец текста.
    text_feed.delete('1.0', tk.END)
    print("Лента сообщений очищена.") # Опционально: вывод в консоль

# --- Создание графического интерфейса ---

# 1. Создаем главное окно приложения
root = tk.Tk()
root.title("Лента сообщений с очисткой") # Устанавливаем заголовок окна

# 2. Создаем виджет Text для отображения сообщений.
#    Он будет выступать в роли нашей "ленты сообщений".
#    Можно использовать tk.Text или tk.scrolledtext.ScrolledText
#    для автоматической прокрутки при переполнении.
text_feed = tk.Text(root, height=15, width=60, wrap='word') # height - строки, width - символы, wrap='word' - перенос по словам
# text_feed = scrolledtext.ScrolledText(root, height=15, width=60, wrap=tk.WORD) # Альтернатива с прокруткой

# Размещаем текстовое поле в окне с небольшим отступом
text_feed.pack(pady=10, padx=10)

# Добавим несколько примеров сообщений для начала
text_feed.insert(tk.END, "Добро пожаловать в ленту сообщений!\n")
text_feed.insert(tk.END, "Это первое тестовое сообщение.\n")
text_feed.insert(tk.END, "Второе сообщение для демонстрации.\n")
text_feed.insert(tk.END, "Еще одно сообщение...\n")
text_feed.insert(tk.END, "-"*30 + "\n") # Разделитель
text_feed.insert(tk.END, "Здесь могут появляться новые уведомления или данные.\n")

# 3. Создаем кнопку "Очистить"
#    text - текст, который будет отображаться на кнопке.
#    command - функция, которая будет вызвана при нажатии на кнопку.
clear_button = tk.Button(root, text="Очистить ленту", command=clear_messages)

# Размещаем кнопку под текстовым полем с отступом
clear_button.pack(pady=5)

# 4. Запускаем главный цикл обработки событий GUI.
#    Эта строка запускает окно и ждет действий пользователя (например, нажатий кнопок).
root.mainloop()